    public String showAllDefine(int k) {
        String s = "";
            String str = Dictionary.words.get(k).getWord_explain();
            String[] words = str.split("\\t");
            for (int j = 0; j < words.length; j++) {
                s += words[j] + "\n";
        }
        return s;
    }
    public static int binarySearch(String word) {
        int left = 0;
        int right = Dictionary.words.size() - 1;
        while (left <= right) {
            int mid = (left + right) / 2;
            if (Dictionary.words.get(mid).getWord_target().compareTo(word) == 0) {
                return mid;
            } else if (Dictionary.words.get(mid).getWord_target().compareTo(word) < 0) {
                left = mid + 1;
            } else {
                right = mid - 1;
            }
        }
        return -1;
    }

