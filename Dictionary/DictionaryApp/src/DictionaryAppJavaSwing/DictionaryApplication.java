    private void jPortSearchKeyReleased(KeyEvent evt) {//GEN-FIRST:event_jPortSearchKeyReleased
        // TODO add your handling code here:\
        DefaultListModel defaultListModel = new DefaultListModel();
        defaultListModel.removeAllElements();
        jDeFine.setText("");
        defaultListModel.addElement(null);
        String wordType = jPortSearch.getText();
        for (int i = 0; i < Dictionary.words.size(); i++) {
            if (Dictionary.words.get(i).getWord_target().startsWith(wordType)) {
                defaultListModel.addElement(Dictionary.words.get(i).getWord_target());
            }
        }
        jList1.setModel(defaultListModel);
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            int i = binarySearch(wordType);
            if (i != -1) {
                jDeFine.setText(showAllDefine(i));    
                } else {
                JOptionPane.showMessageDialog(rootPane, "Không có từ nào để hiển thị!");
            }
        }
        if (wordType.equals("")) {
            defaultListModel.removeAllElements();
            jDeFine.setText("");
            showAllWords();
        }
    }//GEN-LAST:event_jPortSearchKeyReleased

